#upper()

a = "python is a easy to learn"
print(a.upper()) # PYTHON IS A EASY TO LEARN

#lower()

a = "PYTHON IS A EASY TO LEARN"
print(a.lower()) # python is a easy to learn

#swapcase()

a = "python is a easy to learn"
print(a.swapcase()) #  PYTHON IS A EASY TO LEARN

#title()

a = "python is a easy to learn"
print(a.title())  #   Python Is A Easy To Learn


# capitalize()

a = "python is a easy to learn"
print(a.capitalize()) # Python is a easy to learn